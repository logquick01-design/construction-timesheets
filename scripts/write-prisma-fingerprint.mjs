import { createHash } from "crypto";
import { readFileSync, writeFileSync } from "fs";
import { join } from "path";

const root = process.cwd();
const schema = readFileSync(join(root, "prisma/schema.prisma"), "utf8");
const hash = createHash("sha256").update(schema).digest("hex").slice(0, 12);

writeFileSync(
  join(root, "src/lib/prisma-fingerprint.ts"),
  `// Auto-generated by scripts/write-prisma-fingerprint.mjs — do not edit
export const PRISMA_SCHEMA_FINGERPRINT = "${hash}";
`
);

console.log(`Prisma schema fingerprint: ${hash}`);
